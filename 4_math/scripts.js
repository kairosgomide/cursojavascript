// Math.max -> Pega o maior número entre os parâmetros
let maior =Math.max(5, 100, 44, 77, 55, 1000);
console.log(maior);

// Math.min -> Pega o menor número entre os parâmetros
let menor = Math.min(5, 2, 100, 44);
console.log(menor);

// Math.round -> Arredonda um número
let arredondar = Math.round(5.487123421384);
console.log(arredondar);

// Math.ceil -> Arredonda um número para cima
let arredondarParaCima = Math.ceil(5.000000001);
console.log(arredondarParaCima);

// Math.pow -> Calcula o expoente. Tem que entrar com a base, expoente 2³
let expoente = Math.pow(2, 3);
console.log(expoente);

// Math.sqrt -> Calcula a raiz quadrada de um número
let raizQuadrada = Math.sqrt(49);
console.log(raizQuadrada);
